// --- Skips empty values ---

func TestInjectSkipsEmptyValues(t *testing.T) {
	storePath := createVault(t, map[string]string{
		"FILLED":     "has-value",
		"SCAFFOLDED": "",
	})

	stdout, _, err := llmvlt(t, storePath, "inject", "--format", "dotenv")
	if err != nil {
		t.Fatalf("inject failed: %v", err)
	}
	assertContains(t, stdout, "FILLED=has-value", "dotenv output")
	assertNotContains(t, stdout, "SCAFFOLDED", "dotenv output")
}

// --- Output is sorted ---

func TestInjectOutputIsSorted(t *testing.T) {
	storePath := createVault(t, map[string]string{
		"ZEBRA": "z",
		"ALPHA": "a",
		"MIDDLE": "m",
	})

	stdout, _, err := llmvlt(t, storePath, "inject", "--format", "shell")
	if err != nil {
		t.Fatalf("inject failed: %v", err)
	}

	lines := strings.Split(strings.TrimSpace(stdout), "\n")
	if len(lines) != 3 {
		t.Fatalf("expected 3 lines, got %d", len(lines))
	}
	if !strings.Contains(lines[0], "ALPHA") {
		t.Errorf("first line should contain ALPHA, got: %s", lines[0])
	}
	if !strings.Contains(lines[2], "ZEBRA") {
		t.Errorf("last line should contain ZEBRA, got: %s", lines[2])
	}
}

