// --- Output to file ---

func TestInjectOutputToFile(t *testing.T) {
	storePath := createVault(t, map[string]string{
		"KEY_A": "value-a",
		"KEY_B": "value-b",
	})

	outFile := filepath.Join(t.TempDir(), ".env")
	_, stderr, err := llmvlt(t, storePath,
		"inject", "--format", "dotenv", "-o", outFile)

	if err != nil {
		t.Fatalf("inject to file failed: %v\nstderr: %s", err, stderr)
	}
	assertContains(t, stderr, "Written to", "stderr")

	// Verify file contents
	data, err := os.ReadFile(outFile)
	if err != nil {
		t.Fatalf("could not read output file: %v", err)
	}
	content := string(data)
	assertContains(t, content, "KEY_A=value-a", "file content")
	assertContains(t, content, "KEY_B=value-b", "file content")

	// Verify file permissions
	info, _ := os.Stat(outFile)
	if info.Mode().Perm() != 0600 {
		t.Errorf("output file permissions = %o, want 0600", info.Mode().Perm())
	}
}

