// --- Shell format ---

func TestInjectShell(t *testing.T) {
	storePath := createVault(t, map[string]string{
		"API_KEY":    "secret-value",
		"OTHER_KEY":  "other-value",
	})

	stdout, _, err := llmvlt(t, storePath, "inject")
	if err != nil {
		t.Fatalf("inject failed: %v", err)
	}
	assertContains(t, stdout, "export API_KEY=", "shell output")
	assertContains(t, stdout, "export OTHER_KEY=", "shell output")
	assertContains(t, stdout, "secret-value", "shell output")
}

func TestInjectShellExplicitFormat(t *testing.T) {
	storePath := createVault(t, map[string]string{"KEY": "val"})

	for _, format := range []string{"shell", "sh", "bash", "zsh"} {
		stdout, _, err := llmvlt(t, storePath, "inject", "--format", format)
		if err != nil {
			t.Errorf("inject --format %s failed: %v", format, err)
			continue
		}
		assertContains(t, stdout, "export KEY=", format+" output")
	}
}

func TestInjectShellEscapesSingleQuotes(t *testing.T) {
	storePath := createVault(t, map[string]string{
		"KEY": "it's a test",
	})

	stdout, _, err := llmvlt(t, storePath, "inject", "--format", "shell")
	if err != nil {
		t.Fatalf("inject failed: %v", err)
	}
	// Single quotes should be escaped for safe shell eval
	assertContains(t, stdout, `'\''`, "shell escape")
}

